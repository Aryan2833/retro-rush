// Tiny bitmap font — each character is a 3x5 grid of pixels rendered at scaled size.
const Font = (() => {
  const G = {
    'A': ['010','101','111','101','101'],
    'B': ['110','101','110','101','110'],
    'C': ['011','100','100','100','011'],
    'D': ['110','101','101','101','110'],
    'E': ['111','100','110','100','111'],
    'F': ['111','100','110','100','100'],
    'G': ['011','100','101','101','011'],
    'H': ['101','101','111','101','101'],
    'I': ['111','010','010','010','111'],
    'J': ['001','001','001','101','010'],
    'K': ['101','101','110','101','101'],
    'L': ['100','100','100','100','111'],
    'M': ['101','111','111','101','101'],
    'N': ['101','111','111','111','101'],
    'O': ['010','101','101','101','010'],
    'P': ['110','101','110','100','100'],
    'Q': ['010','101','101','111','011'],
    'R': ['110','101','110','101','101'],
    'S': ['011','100','010','001','110'],
    'T': ['111','010','010','010','010'],
    'U': ['101','101','101','101','011'],
    'V': ['101','101','101','010','010'],
    'W': ['101','101','111','111','101'],
    'X': ['101','101','010','101','101'],
    'Y': ['101','101','010','010','010'],
    'Z': ['111','001','010','100','111'],
    '0': ['010','101','101','101','010'],
    '1': ['010','110','010','010','111'],
    '2': ['110','001','010','100','111'],
    '3': ['110','001','010','001','110'],
    '4': ['101','101','111','001','001'],
    '5': ['111','100','110','001','110'],
    '6': ['011','100','110','101','010'],
    '7': ['111','001','010','010','010'],
    '8': ['010','101','010','101','010'],
    '9': ['010','101','011','001','110'],
    ':': ['000','010','000','010','000'],
    '-': ['000','000','111','000','000'],
    '.': ['000','000','000','000','010'],
    ',': ['000','000','000','010','100'],
    '!': ['010','010','010','000','010'],
    '?': ['110','001','010','000','010'],
    '/': ['001','001','010','100','100'],
    '(': ['010','100','100','100','010'],
    ')': ['010','001','001','001','010'],
    '*': ['000','101','010','101','000'],
    '+': ['000','010','111','010','000'],
    "'": ['010','010','000','000','000'],
    '"': ['101','101','000','000','000'],
    ' ': ['000','000','000','000','000'],
  };

  function draw(ctx, text, x, y, scale = 2, color = '#fff', shadow = '#000') {
    text = String(text).toUpperCase();
    let cx = x;
    for (const ch of text) {
      const glyph = G[ch] || G['?'];
      if (shadow) drawGlyph(ctx, glyph, cx + 1, y + 1, scale, shadow);
      drawGlyph(ctx, glyph, cx, y, scale, color);
      cx += (3 + 1) * scale; // 1px gap
    }
    return cx - x;
  }

  function drawGlyph(ctx, glyph, x, y, scale, color) {
    ctx.fillStyle = color;
    for (let row = 0; row < 5; row++) {
      const line = glyph[row];
      for (let col = 0; col < 3; col++) {
        if (line[col] === '1') {
          ctx.fillRect(x + col * scale, y + row * scale, scale, scale);
        }
      }
    }
  }

  function measure(text, scale = 2) {
    return String(text).length * (3 + 1) * scale - scale;
  }

  function drawCentered(ctx, text, cx, y, scale = 2, color = '#fff', shadow = '#000') {
    const w = measure(text, scale);
    return draw(ctx, text, cx - w / 2, y, scale, color, shadow);
  }

  return { draw, drawCentered, measure };
})();
